<h1 class="h1">Progetto</h1>
<h2 id="descrizione" class="h2">Descrizione del progetto</h2>

<p>
	Il progetto <em>Arcipelago Ceresa</em> mira a rendere accessibile, ricontestualizzare e
	valorizzare l’eredità letteraria della scrittrice italo-svizzera Alice Ceresa, la cui opera e
	attività intellettuale, pur al centro di un rinnovato interesse critico, restano in larga parte
	inesplorate. Proponendo un’edizione digitale delle opere inedite, disperse e postume, il progetto
	recupera e riunisce una parte significativa del corpus di testi ceresiani in un’unica piattaforma
	digitale ad accesso aperto (<em>Open Access</em>), che consentirà di sviluppare nuove prospettive
	di ricerca.
</p>
<p>
	Nel contesto di un corpus testuale eterogeneo (narrativa, saggistica, teatro e poesia), sarà
	fornita un’ampia selezione di scritti dispersi (che include la produzione giornalistica), inediti
	(saranno proposte parti significative di una trilogia di romanzi più volte annunciata ma mai
	completata) e postumi (tra cui il <em>Piccolo dizionario dell’inuguaglianza femminile</em>, in
	un’edizione ampliata e arricchita anche da materiali audio).
</p>
<p>
	La presenza di una componente multimediale riflette la peculiare configurazione dell’opera di
	Ceresa, la cui trasmissione non è rimasta circoscritta all’interno della cultura manoscritta e a
	stampa, ma si è estesa alla diffusione radiofonica. Saranno pertanto rese disponibili anche le
	letture radiofoniche di testi (in parte inediti) di Ceresa prodotte dalla Radio svizzera di lingua
	italiana alla fine degli anni Settanta.
</p>
<p>
	L’impegno editoriale sarà accompagnato da quattro progetti legati a due macroaree di ricerca:
	plurilinguismo e transnazionalità da un lato, <em>gender</em> e genere letterario dall’altro.
</p>
<p>
	Il gruppo di ricerca affiliato all’Università di Berna si focalizzerà sulla ricostruzione del
	profilo plurilingue dell’autrice, formatasi all’incrocio di lingue diverse (italiano, tedesco e
	francese), e sull’analisi dell’impatto esercitato da tale identità linguistica sulle sue pratiche
	creative. Saranno inoltre approfonditi i processi e le dinamiche transnazionali legate
	all’attività ceresiana, prestando in particolare attenzione al suo impegno nell’ambito della
	traduzione e al suo ruolo di mediatrice culturale.
</p>
<p>
	Il gruppo dell’Università di Zurigo, attraverso un approccio che si avvarrà delle prospettive
	filosofiche contemporanee sul genere, delle teorie decoloniali e dei <em>lesbian studies</em> degli
	anni Settanta, riprenderà e approfondirà le ricerche esistenti sullo sperimentalismo e sul femminismo
	nell’opera di Ceresa. Queste linee di ricerca saranno integrate con un’analisi della decostruzione
	dei generi letterari, intesa come liberazione dai vincoli della sessualità binaria e dalle forme di
	normatività di genere.
</p>

<h2 id="cooperazioni" class="h2">Cooperazioni</h2>
<h3 class="h3">Data Science Lab (Berna)</h3>
<p>
	Il progetto dell’edizione digitale verrà realizzato in stretta collaborazione con il Data Science
	Lab (DSL) dell’Università di Berna, e si avvarrà in particolare del supporto di Peter Dängeli e
	Levyn Andrey Bürki.
</p>
<p>
	Al DSL verranno affidati la gestione e conservazione del materiale digitalizzato tramite server
	IIIF, lo sviluppo di prototipi, il supporto per le fasi di trascrizione e di codifica dei testi.
	Il DSL sarà inoltre responsabile della realizzazione delle componenti tecniche avanzate, tra cui
	la configurazione del workflow, l’implementazione del modello dati TEI e la trasformazione
	automatica dei testi OCR in formato TEI. Il laboratorio seguirà inoltre la finalizzazione
	dell’infrastruttura tecnica in vista del rilascio ufficiale dell’edizione digitale.
</p>
<p>
	Il DSL svolgerà anche un ruolo attivo nella progettazione dell’applicazione web, nello sviluppo
	delle interfacce interne ed esterne (front-end), nella gestione della distribuzione dei dati e
	nella preparazione del lancio pubblico.
</p>

<h3 class="h3">Archivio svizzero di letteratura (ASL)</h3>

<p>
	L’Archivio svizzero di letteratura (ASL) della Biblioteca nazionale svizzera, con sede a Berna, è
	partner importante del progetto. Nel 2003 la Confederazione Svizzera ha infatti acquisito il
	<a href="https://ead.nb.admin.ch/html/ceresa.html">lascito di Alice Ceresa</a>, di cui detiene
	interamente i diritti dal 2024. Per questo motivo, gran parte del corpus testuale che confluirà
	nel progetto editoriale è stato selezionato dal patrimonio archivistico dell’ASL, che supporta
	attivamente le ricerche e fornisce le riproduzioni da includere nell’edizione digitale.
</p>

<h2 class="h2">English</h2>

<h3 class="h3">Project description</h3>
<p>
	The <em>Arcipelago Ceresa</em> project aims to make accessible, recontextualize, and highlight the
	literary legacy of the Swiss-Italian writer Alice Ceresa, whose literary work and intellectual
	activity – despite attracting a renewed interest in recent years – remain largely unexplored. By
	proposing a multimedia digital edition of her unpublished, dispersed, and posthumous works, the
	project recovers and brings together a significant portion of Ceresa’s corpus on a single
	open-access digital platform, thereby enabling the development of new research perspectives.
	Within the framework of a heterogeneous body of work (including fiction, essays, theatre, and
	poetry), the edition will offer an extensive selection of dispersed writings (such as her
	journalistic production), unpublished texts (including substantial portions of a planned trilogy
	of novels repeatedly announced but never completed), and posthumous works (among them the
	<em> Piccolo dizionario dell’inuguaglianza femminile,</em> presented here in an expanded and
	enriched edition with previously unpublished entries and the original audio recordings broadcast
	by the Radio svizzera di lingua italiana in 1978). The multimedia component reflects the
	distinctive configuration of Ceresa’s <em>oeuvre</em>, which was not confined to manuscript and
	print culture but also extended to the radio transmission of her works. The project will therefore
	incorporate radio recordings of Ceresa’s texts (some unpublished), produced by RSI in the late
	1970s.
</p>

<p>
	The editorial work will be accompanied by four research initiatives organized around two broad
	thematic areas: multilingualism and transnationalism on the one hand, and gender and literary
	genre on the other. The research group at the University of Bern will focus on reconstructing the
	author’s multilingual profile – shaped at the intersection of Italian, German, and French – and on
	analyzing the impact of this linguistic identity on her creative practices. The study will also
	examine the transnational processes and dynamics connected to Ceresa’s activities, with particular
	attention to her work as a translator and her role as a cultural mediator.
</p>

<p>
	The research group at the University of Zurich will expand upon existing research on
	experimentalism and feminism in Ceresa’s work, combining these lines of inquiry with an
	examination of her deconstruction of literary genres as an act of liberation from the constraints
	of binary sexuality and gender normativity. This investigation will draw on contemporary
	philosophical perspectives on gender, decolonial theories, and the lesbian studies of the 1970s.
</p>

<h3 class="h3">Cooperations</h3>
<h4>Data Science Lab (DSL)</h4>
<p>
	The digital edition project is being carried out in close collaboration with the Data Science Lab
	(DSL), specifically with Peter Dängeli (UniBe) and Levyn Andrey Bürki (UniBe).
</p>

<p>
	The DSL will be entrusted with the management and preservation of digitized materials via IIIF
	servers, the development of prototypes, as well as support for the transcription and encoding
	process. The DSL is responsible for developing the advanced technical components, including the
	configuration of the workflow, the implementation of the TEI data model, and the automated
	transformation of OCR outputs into TEI format. The lab will also oversee the completion of the
	technical infrastructure in preparation for the official release of the digital edition.
</p>

<p>
	In addition, the DSL plays an active role in designing the web application, developing both
	internal and external (front-end) interfaces, managing data distribution, and preparing for the
	public launch.
</p>

<h4>Swiss Literary Archives (Bern)</h4>
<p>
	The Swiss Literary Archives (SLA) of the Swiss National Library are a key partner in the project.
	In 2003, the Swiss Confederation acquired the
	<a href="https://ead.nb.admin.ch/html/ceresa.html">estate of Alice Ceresa</a>, on which it has
	held the rights since 2024. For this reason, a substantial part of the project’s corpus has been
	selected from the SLA’s archival holdings, and the SLA also supports the research activities. The
	National Library furthermore provides the reproductions to be included in the digital edition.
</p>
