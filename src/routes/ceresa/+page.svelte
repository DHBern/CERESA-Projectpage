<h1 class="h1">Alice Ceresa</h1>
<p>
	Alice Ceresa (Basilea, 25 gennaio 1923 - Roma, 22 dicembre 2001) trascorre l’infanzia tra Basilea
	e Bellinzona, dove, per volontà paterna, compie studi commerciali e, per passione, inizia a
	scrivere, collaborando con il quotidiano «Il Dovere». Nei primi anni ’40 si trasferisce a Zurigo,
	dove collabora con riviste e quotidiani come «Die Weltwoche» e «Svizzera italiana», entrando in
	contatto con figure quali Franco Fortini e Ignazio Silone. In quegli stessi anni frequenta anche
	la scrittrice, pianista e psicoanalista junghiana Aline Valangin, che in quel periodo accoglie
	nella sua casa di Comologno, nel Canton Ticino, numerosi intellettuali esuli. Dopo soggiorni in
	Francia e nella Svizzera francese, grazie alla mediazione di Silone, nel 1950 si stabilisce a
	Roma, dove pubblica su riviste come «Botteghe Oscure» e si avvicina agli ambienti dell’avanguardia
	italiana, mantenendo tuttavia una forte autonomia stilistica e concettuale. Parallelamente svolge
	un’intensa attività di traduttrice dal tedesco, spesso in collaborazione con l’editore Longanesi,
	traducendo in italiano opere di autori come Elias Canetti e Gerold Späth.
</p>

<p>
	Come narratrice esordisce nel 1943 con il racconto <em>Gli altri</em>, apparso sulla rivista
	«Svizzera italiana», e si afferma nel 1967 con il romanzo sperimentale <em>La figlia prodiga</em>
	(Einaudi), primo titolo della collana “La ricerca letteraria” e vincitore nello stesso anno del Premio
	Viareggio Opera Prima. Nel 1979 pubblica sulla rivista «Nuovi Argomenti» il racconto
	<em>La morte del padre</em>
	e nel 1990 il romanzo Bambine (Einaudi), vincitore del Premio Schiller, che affronta con sguardo lucido
	e corrosivo il tema dell’infanzia e della struttura patriarcale. A partire dagli anni Settanta lavora
	al <em>Piccolo dizionario dell’inuguaglianza femminile</em>, pubblicato postumo da nottetempo
	(2007, 2020).
</p>

<p>
	La sua opera, centrata sulla condizione femminile e sulla ricerca di forme linguistiche capaci di
	rappresentarla, coniuga rigore formale, ironia e tensione etica. Oggi Alice Ceresa è riconosciuta
	come una voce fondamentale della scrittura femminista e sperimentale del Novecento. Tradotti in
	francese e in tedesco, i suoi scritti principali conoscono negli ultimi anni nuove edizioni e un
	rinnovato interesse critico.
</p>

<h2 class="h2">English</h2>

<p>
	Alice Ceresa (Basel, 25 January 1923 - Rome, 22 December 2001) spends her childhood between Basel
	and Bellinzona. At her father’s insistence, she pursues commercial studies, but starts to write
	out of personal passion, contributing to the daily newspaper «Il Dovere». In the early 1940s she
	moves to Zurich, where she collaborates with magazines and newspapers such as «Die Weltwoche» and
	«Svizzera italiana». During this period, she meets Franco Fortini and Ignazio Silone, and
	befriends the writer, pianist, and Jungian psychoanalyst Aline Valangin, who in those difficult
	years is hosting many exiled intellectuals at her home in Comologno, Canton Ticino.
</p>

<p>
	After periods in France and in French-speaking Switzerland, and through Silone’s mediation, Ceresa
	settles in Rome in 1950. There, she publishes in magazines such as «Botteghe Oscure», and later
	becomes involved with the Italian avant-garde, while always preserving a strong stylistic and
	conceptual independence. She is also active as a translator from German, often working with the
	publisher Longanesi and introducing Italian readers to works by authors such as Elias Canetti and
	Gerold Späth.
</p>

<p>
	Ceresa makes her fiction debut in 1943 with the short story <em>Gli altri</em>, published in
	«Svizzera italiana». She rises to prominence in 1967 with the experimental novel
	<em>La figlia prodiga</em>
	(Einaudi), the first title in the “La ricerca letteraria” series and winner of the Viareggio Prize
	for Opera prima that same year. In 1979 she publishes the short story <em>La morte del padre</em>
	in «Nuovi Argomenti», and in 1990 the novel Bambine (Einaudi), which wins the Schiller Prize and offers
	an acute and pungent portrayal of childhood and patriarchal structures. From the 1970s onwards, she
	works on the <em>Piccolo dizionario dell’inuguaglianza femminile</em>, published posthumously
	(nottetempo 2007, 2020).
</p>

<p>
	Her work, centred on the female condition and on the search for linguistic forms capable of
	representing it, combines formal rigour, irony, and ethical tension. Today, Alice Ceresa is
	recognised as a significant voice in twentieth-century feminist and experimental writing.
	Translated into French and German during her lifetime, her major works have recently been
	reissued, and her writing is met with renewed critical interest.
</p>
